<div class="bio">

	<p>Canadian Dubstep duo, Orbatak, consists of Junglists and EDM DJ/Producers, Dirty Tactix and L-Nix. They have released, 
to date, tracks on Badman digital, Bass Punch Records, BetaMorph Records, Stop/Start Records, FoulplayDubstep and 
Dubstep Division Records and have remixed tracks for Envy(Num. 5 on the UK HipHop charts), Spektrum(UK), Heavy Deviance (UK),
Mark Instinct and Don Goliath which have been well received by DJs and punters alike.
</p>
<p>Orbatak's music has been featured on Annie Nightingales BBC Radio 1 show, as well as appearances on the Dubsteppers
Delight Radio Show on sensimedia.net, The Afterdark Radio Show on sub.fm, the Dank Dealz Radio Show on Dubstep.fm, and on Bass 
Box Radio. Their tracks have seen support from the likes of Excision, Datsik, Elite Force, Chimpo, Ultrablack, RiskoTh&egrave;que,
Vaski, Liquid Stranger, Mark Instinct, Downlink(Rottun), Tomba, Borgore, Davr and Ganja White Night to name a few.
</p>	
<p>	Orbatak has played alongside some of the biggest names in the international underground music scene including Excision, Downlink, 
Mark Instinct, Ed Rush &amp; Optical, Ewun, Capital J, DJ DeeKline, Adam Freeland, Nick Thayer, Atomic Hooligan, Fort Knox 5, Ill Gates,
Perfect Combination UK, DJ Czech, and many many more.They have thrown raves, after-hours party's and club nights, under their 
'Those Damn Kids!' production company, for the last 12 years at various clubs including the now defunct Fluid Lounge, Club 198, 
Spiral Source and Velvet Underground as well as current night clubs Spirit Bar and The Royal and have played several bass-heavy brain 
rattling sets at the world-famous Shambhala Music Festival in their hometown in British Columbia, Canada.
</p>
